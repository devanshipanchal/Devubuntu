num1 = int(input('Enter first number: '))
num2 = int(input('Enter second number: '))

# addition
add = num1 + num2
print("Addition", add)
# subtraction
sub = num1 - num2
print("Subtraction", sub)
# Multiplication
multi = num1 * num2
print("Multiplication", multi)
# division
div = num1 / num2
print("Division", div)
# floor division
floordiv = num1 // num2
print("Floor Division", floordiv)
# modulo
mod = num1 % num2
print("Modulus", mod)
# power
exp = num1 ** num1
print("Exponent", exp)


